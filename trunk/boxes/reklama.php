<h3>Reklama na autobusach</h3>

Reklama na autobusach ZKM Sp. z o.o. w Lęborku
Cennik i warunki ogólne.
Obowiązuje od dnia 08.09.2008r.

   1. Zamieszczenie reklamy zewnętrznej polega na jej naniesieniu na powierzchnię autobusu techniką materiałów samoprzylepnych po uprzednim zaakceptowaniu przez ZKM Sp. z o.o. w Lęborku projektu.
   2. Materiały samoprzylepne można stosować na powierzchni oblachowania i na powierzchni szyb autobusu w następujący sposób:
          * na powierzchni szyb - wyłącznie folia perforowana,
          * na powierzchni oblachowania autobusu - dowolny rodzaj folii.
   3. Powierzchnie niepodlegające oklejeniu:
          * boczne okno kierowcy,
          * szyba przednia,
          * szyby drzwi przednich,
          * tablice informujące o numerze linii obsługiwanej przez autobus,
          * tył autobusu.
   4. Reklama eksponowana na autobusie nie może być sprzeczna z obowiązującymi przepisami, ani naruszać swą treścią i symboliką praw osób trzecich.
   5. Koszty projektu, wykonania i montażu ponosi Zleceniodawca.
   6. W przypadku umieszczenia reklamy na jednym z boków autobusu, nie jest gwarantowana wyłączność reklamy na tym pojeździe.
   7. W każdym przypadku ekspozycji reklamy Zleceniodawca jest z zobowiązany do likwidacji reklamy i przywrócenia pojazdom barw ZKM oraz do uzupełnienia ewentualnie powstałych ubytków powierzchni lakierniczej.
   8. Minimalny okres ekspozycji reklamy zewnętrznej 6 miesięcy.
   9. Reklama na okres krótszy niż 6 miesięcy - cena reklamy ustalana indywidualnie.

