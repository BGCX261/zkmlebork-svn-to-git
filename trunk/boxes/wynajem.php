<h3>Wynajem autobusów</h3>

Warunki i cennik wynajmu autobusów
Zakładu Komunikacji Miejskiej Spółka z o.o. w Lęborku

Obowiązuje od dnia 08.09.2008r.

Wynajem na terenie miasta Lęborka (obsługa pogrzebów, wesel itp.):

    * Stawka za 1 godzinę wynajmu: 50,00 zł netto
    * Za wynajęcie autobusu obciąża się za minimum 1 godzinę
    * Po przekroczeniu 1 godziny opłata będzie naliczana za każdy następny rozpoczęty kwadrans w kwocie 1/4 ceny usługi za 1 godz.
    * Czas liczony jest od przyjazdu autobusu na miejsce podstawienia do miejsca zakończenia wynajmu
    * Przy odstąpieniu od zleconej usługi w dniu jej realizacji pobierana jest opłata, jak za 1 godzinę wynajmu

Wynajem poza teren miasta Lęborka (wycieczki zakładowe, spotkania integracyjne itp.):

    * Stawka za 1 km : 6,00 zł netto
    * Stawka za 1 godzinę postoju : 30,00 zł netto
    * Za wynajęcie autobusu obciąża się za minimum 10 km
    * Po przekroczeniu 1 godziny postoju opłata będzie naliczana za każdy następny rozpoczęty kwadrans w kwocie 1/4 ceny usługi za 1 godz.
    * Miejscem podstawienia i zakończenia wynajmu jest miasto Lębork
    * Czas liczony jest od przyjazdu autobusu na miejsce podstawienia do miejsca zakończenia wynajmu

Wynajem dla szkół na terenie miasta Lęborka (przewozy na basen itp.):

    * Stawka za 1 km: 4,00 zł netto
    * Wynajem realizowany jest po podpisaniu umowy

Aby dokonać zamówienia należy złożyć zamówienie w siedzibie Zakładu Komunikacji Miejskiej Spółka z o.o: Lębork ul. Łokietka 5 lub za pomocą fax 0-59 862 14 51.
